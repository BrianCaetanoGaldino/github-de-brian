num1 = int(input("digite um numero "))

num2 = int(input("digite mais um numero "))
   
sinal = input("digite um sinal (+,-,/,*) ")

if(sinal == "+"):
    numR = num1 + num2
    print(numR)
elif(sinal == "-"):
    numR = num1 - num2
    print(numR)
elif(sinal == "/"):
    numR = num1 / num2
    print(numR)
elif(sinal == "*"):
    numR = num1 * num2
    print(numR)

    

